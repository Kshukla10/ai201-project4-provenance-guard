import os
import uuid
import json
from datetime import datetime, timezone

from dotenv import load_dotenv
from flask import Flask, jsonify, request
from flask_limiter import Limiter
from flask_limiter.util import get_remote_address
from groq import Groq

load_dotenv()

app = Flask(__name__)

limiter = Limiter(
    get_remote_address,
    app=app,
    default_limits=[],
    storage_uri="memory://",
)

groq_client = Groq(api_key=os.getenv("GROQ_API_KEY"))

LOG_FILE = "audit_log.json"


# ── Audit log helpers ──────────────────────────────────────────────

def read_log():
    if not os.path.exists(LOG_FILE):
        return []
    with open(LOG_FILE, "r") as f:
        return json.load(f)


def write_log(entries):
    with open(LOG_FILE, "w") as f:
        json.dump(entries, f, indent=2)


def append_entry(entry):
    entries = read_log()
    entries.append(entry)
    write_log(entries)


# ── Signal 1: Groq LLM classifier ─────────────────────────────────

def groq_signal(text):
    """
    Asks the LLM to score how likely the text is AI-generated.
    Returns a float from 0.0 (human) to 1.0 (AI).
    """
    prompt = f"""You are an expert at detecting whether text was written by a human or generated by an AI.

Analyze the following text and respond with ONLY a JSON object in this exact format:
{{"ai_probability": <float between 0.0 and 1.0>, "reasoning": "<one sentence>"}}

0.0 means certainly human-written. 1.0 means certainly AI-generated.

Text to analyze:
\"\"\"{text}\"\"\"
"""
    response = groq_client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.1,
    )

    raw = response.choices[0].message.content.strip()

    # Strip markdown code fences if present
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    raw = raw.strip()

    result = json.loads(raw)
    score = float(result["ai_probability"])
    return max(0.0, min(1.0, score))  # clamp to [0, 1]


# ── Routes ─────────────────────────────────────────────────────────

@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json()

    if not data or "text" not in data or "creator_id" not in data:
        return jsonify({"error": "Request must include 'text' and 'creator_id'"}), 400

    text = data["text"].strip()
    creator_id = data["creator_id"]

    if len(text) < 20:
        return jsonify({"error": "Text too short for analysis"}), 400

    content_id = str(uuid.uuid4())

    # Signal 1 only for now (Signal 2 added in Milestone 4)
    llm_score = groq_signal(text)

    # Placeholder confidence and label until M4
    confidence = round(llm_score, 4)
    attribution = "likely_ai" if confidence >= 0.65 else ("likely_human" if confidence <= 0.35 else "uncertain")
    label = f"[Placeholder label — confidence {confidence}]"

    entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylo_score": None,
        "status": "classified",
        "appeal_reasoning": None,
    }
    append_entry(entry)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "label": label,
    })


@app.route("/log", methods=["GET"])
def get_log():
    entries = read_log()
    return jsonify({"entries": entries[-20:]})  # return last 20


if __name__ == "__main__":
    app.run(debug=True)